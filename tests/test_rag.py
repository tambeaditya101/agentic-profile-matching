class TestGetFullResume:
    def test_get_full_resume_returns_text(
        self, retriever: ResumeRetriever, indexed_store: object
    ) -> None:
        """get_full_resume should return non-empty text for a known candidate."""
        # First, find a candidate_id
        results = retriever.search("React developer", top_k=1)
        assert len(results) >= 1
        candidate_id = results[0]["candidate_id"]

        full_text = retriever.get_full_resume(candidate_id)
        assert full_text is not None
        assert len(full_text) > 100, "Full resume text too short"

    def test_get_full_resume_contains_key_info(
        self, retriever: ResumeRetriever, indexed_store: object
    ) -> None:
        """The full resume should contain the candidate's key skills."""
        # Find Alice (React developer)
        results = retriever.search("React developer", top_k=1)
        candidate_id = results[0]["candidate_id"]
        full_text = retriever.get_full_resume(candidate_id)

        assert "React" in full_text, "React skill not found in full resume"
        assert "Alice" in full_text or "ALICE" in full_text, "Name not in full resume"

    def test_get_full_resume_nonexistent(self, retriever: ResumeRetriever) -> None:
        """Nonexistent candidate_id should return None."""
        result = retriever.get_full_resume("nonexistent_candidate_12345")
        assert result is None


