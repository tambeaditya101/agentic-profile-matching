# 2. Text Chunking Tests
# ===================================================================


class TestChunking:
    def test_chunk_count(self, sample_pdf_path: Path) -> None:
        """A ~2000 char resume should produce multiple chunks."""
        text = ResumeIndexer._extract_text(sample_pdf_path)
        indexer = ResumeIndexer(chunk_size=500, chunk_overlap=100)
        chunks = indexer._chunk_text(text)
        # With ~2000 chars and 500 char chunks with 100 overlap (step=400),
        # we expect at least 3 chunks
        assert len(chunks) >= 3, f"Expected 3+ chunks, got {len(chunks)}"

    def test_chunk_size_respected(self, sample_pdf_path: Path) -> None:
        """No chunk should exceed chunk_size by more than a small margin."""
        text = ResumeIndexer._extract_text(sample_pdf_path)
        indexer = ResumeIndexer(chunk_size=1000, chunk_overlap=200)
        chunks = indexer._chunk_text(text)
        for chunk in chunks:
            assert len(chunk["text"]) <= 1050, (
                f"Chunk {chunk['index']} exceeds chunk_size: "
                f"{len(chunk['text'])} > 1050"
            )

    def test_chunk_indices_sequential(self, sample_pdf_path: Path) -> None:
        """Chunk indices should be 0, 1, 2, ... N-1."""
        text = ResumeIndexer._extract_text(sample_pdf_path)
        indexer = ResumeIndexer(chunk_size=500, chunk_overlap=100)
        chunks = indexer._chunk_text(text)
        indices = [c["index"] for c in chunks]
        assert indices == list(range(len(chunks)))

    def test_empty_text_returns_no_chunks(self) -> None:
        """Empty or whitespace-only text should return empty list."""
        indexer = ResumeIndexer()
        assert indexer._chunk_text("") == []
        assert indexer._chunk_text("   \n  ") == []

    def test_short_text_single_chunk(self) -> None:
        """Text shorter than chunk_size should produce exactly 1 chunk."""
        indexer = ResumeIndexer(chunk_size=1000, chunk_overlap=200)
        chunks = indexer._chunk_text("Hello, this is a short resume.")
        assert len(chunks) == 1


