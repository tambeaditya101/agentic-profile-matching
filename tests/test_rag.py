class TestSearch:
    def test_search_react_developer(
        self, retriever: ResumeRetriever, indexed_store: object
    ) -> None:
        """Search for 'React developer' should return Alice Johnson (React dev)."""
        results = retriever.search("React developer with 3 years experience", top_k=3)
        assert len(results) >= 1, "Search returned no results"
        names = [r["name"] for r in results]
        assert any("Alice" in n for n in names), (
            f"Expected Alice (React dev) in results, got: {names}"
        )

    def test_search_python_backend(
        self, retriever: ResumeRetriever, indexed_store: object
    ) -> None:
        """Search for 'Python backend' should return Bob Smith."""
        results = retriever.search("Python backend Django FastAPI engineer", top_k=3)
        assert len(results) >= 1
        names = [r["name"] for r in results]
        assert any("Bob" in n for n in names), (
            f"Expected Bob (Python backend) in results, got: {names}"
        )

    def test_search_data_scientist(
        self, retriever: ResumeRetriever, indexed_store: object
    ) -> None:
        """Search for 'data scientist machine learning' should return Carol."""
        results = retriever.search(
            "data scientist machine learning NLP neural networks", top_k=3
        )
        assert len(results) >= 1
        names = [r["name"] for r in results]
        assert any("Carol" in n for n in names), (
            f"Expected Carol (Data Scientist) in results, got: {names}"
        )

    def test_search_devops(
        self, retriever: ResumeRetriever, indexed_store: object
    ) -> None:
        """Search for 'DevOps Kubernetes' should return David."""
        results = retriever.search(
            "DevOps Kubernetes AWS cloud infrastructure engineer", top_k=3
        )
        assert len(results) >= 1
        names = [r["name"] for r in results]
        assert any("David" in n for n in names), (
            f"Expected David (DevOps) in results, got: {names}"
        )

    def test_search_returns_standardized_format(self, retriever: ResumeRetriever) -> None:
        """Each result should have the expected keys."""
        results = retriever.search("React developer", top_k=3)
        for r in results:
            assert "candidate_id" in r
            assert "name" in r
            assert "score" in r
            assert "excerpt" in r
            assert isinstance(r["score"], (int, float))

    def test_search_deduplication(self, retriever: ResumeRetriever) -> None:
        """Results should be deduplicated by candidate_id."""
        results = retriever.search("developer", top_k=10)
        candidate_ids = [r["candidate_id"] for r in results]
        assert len(candidate_ids) == len(set(candidate_ids)), (
            f"Duplicate candidate_ids found: {candidate_ids}"
        )


